def build_confirm_question(draft: dict[str, Any]) -> str:
    grade = draft.get("grade", "未知")
    subjects = "、".join(draft.get("subjects", [])) or "未填写"
    goal = draft.get("goal", "未填写")
    exam = draft.get("next_exam_name", "")
    exam_date = draft.get("next_exam_date", "")
    exam_str = f"{exam}（{exam_date}）" if exam else "暂无"

    return (
        "好的，我已经了解了你的情况，来确认一下：\n\n"
        f"学年：{grade}\n"
        f"科目：{subjects}\n"
        f"目标：{goal}\n"
        f"近期考试：{exam_str}\n\n"
        "确认无误的话，我就开始为你建立专属知识库和学习档案！\n"
        "（说【确认】即可，也可以告诉我需要修改的地方）"
    )


# ── 每步数据提取 prompt ───────────────────────────────────────

def extract_prompt(step: str, user_message: str, draft: dict[str, Any]) -> tuple[str, str]:
    """返回 (system, user_prompt)，LLM 需要返回 JSON。"""

    system = (
        "你是一个信息提取助手。从用户的回复中提取结构化信息，"
        "严格返回合法 JSON，不要加任何额外说明或 markdown 代码块。"
    )

    if step == "grade":
        prompt = (
            "从下面的回复中提取年级信息，返回 JSON：\n"
            '{"grade": "初一|初二|初三|高一|高二|高三|大一|大二|大三|大四|其他", '
            '"grade_type": "junior|senior|university"}\n\n'
            f"用户回复：{user_message}"
        )

    elif step == "subjects":
        prompt = (
            "从下面的回复中提取科目列表，科目名使用中文（数学/语文/英语/物理/化学/生物/历史/地理/政治/其他）。\n"
            '返回 JSON：{"subjects": ["数学", "英语", ...]}\n\n'
            f"用户回复：{user_message}"
        )

    elif step == "progress":
        subjects_str = str(draft.get("subjects", []))
        prompt = (
            f"用户已学科目：{subjects_str}\n"
            "从下面的回复中，提取每个科目学到的章节/进度。\n"
            '返回 JSON：{"progress": {"数学": "二次函数", "英语": "必修二第三单元", ...}}\n'
            "如果某科目未提及，不要包含该科目。\n\n"
            f"用户回复：{user_message}"
        )

    elif step == "performance":
        subjects_str = str(draft.get("subjects", []))
        prompt = (
            f"用户科目：{subjects_str}\n"
            "从下面的回复中，提取每个科目的成绩水平，值为：优秀/良好/中等/较差 之一。\n"
            '返回 JSON：{"performance": {"数学": "中等", "英语": "良好", ...}}\n'
            "如果某科目未提及，不要包含该科目。\n\n"
            f"用户回复：{user_message}"
        )

    elif step == "next_exam":
        prompt = (
            "从下面的回复中提取考试信息。如果用户说没有考试，exam_name 返回 null。\n"
            '返回 JSON：{"next_exam_name": "期末数学考试|null", '
            '"next_exam_date": "2026-06-20|null", "next_exam_subject": "数学|null"}\n'
            "日期格式 YYYY-MM-DD，如果只说了模糊表述，尽量推断具体日期（当前年份2026）。\n\n"
            f"用户回复：{user_message}"
        )

    elif step == "goal":
        prompt = (
            "从下面的回复中提取用户的学习目标（一句话总结）。\n"
            '返回 JSON：{"goal": "..."}\n\n'
            f"用户回复：{user_message}"
        )

    elif step in ("upload", "confirm"):
        prompt = '{"acknowledged": true}'

    else:
        prompt = '{"acknowledged": true}'

    return system, prompt


